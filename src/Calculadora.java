/**
 * 
 * Calculadora 
 * 
 * Realiza as quatros operações da matemática
 * 
 * @param {number} x - Primeiro número 
 * @param {number} y - Segundo número
 * @return {number} Resultado da operação 
 */
package src;

public class Calculadora{

    public final double somar(double x, double y){return x + y; }

    public final double multiplicar(double x, double y){return x * y; } 

    public final double subtrair(double x, double y){return x - y; } 

    public final double dividir(double x, double y){return x / y; } 

}
